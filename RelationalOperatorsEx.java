public class RelationalOperatorsEx {
    public static void main(String[] args) {
        int a = 11;
        int b = 12;
        int c = 12;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(c <= b);
        System.out.println(c == b);
        System.out.println(a != b);

    }
}
